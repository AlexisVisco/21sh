#include "shell.h"

int		main(void)
{
	char *str;

	while ((str = readline("shell> ")))
	{
		ft_printf("You wrote: {%s}\n", str);
		if (ft_strequ("exit", str))
		{
			free(str);
			exit(0);
		}
		free(str);
	}
}
