#include "shell.h"

void		escape_useless_backquotes(char *s)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == '`' && s[i + 1] && s[i + 1] == '`')
		{
			s[i] = ' ';
			s[i + 1] = ' ';
		}
		i++;
	}
}
